import type { TemplateConfig } from './types.js';

const DEFAULT_CHAPTERS = `
根据项目类型，从以下章节中选择并组织内容：

【安装】—— 前置条件 + 安装命令 + 验证命令
【快速开始】—— 最常用的 2-3 个操作，每个配代码示例
【特性介绍】—— 4-6 项，每项：功能名 + 一句话 + 适用场景
【API 参考】(库/工具必选) —— 核心函数签名、参数、返回值
【命令参考】(CLI 必选) —— 子命令、常用选项、示例
【技术架构】(Web应用/框架推荐) —— 分层说明，核心模块
【项目结构】(大型项目推荐) —— 目录树 + 说明
【配置说明】(需要配置的项目) —— 配置项、默认值
【贡献指南】(必选) —— 环境搭建、运行测试、提PR流程
【许可证】(必选)

不要逐章照抄这个列表。根据项目类型选择最重要的章节，组织成自然的阅读顺序。`;

export function buildPromptFromConfig(config: TemplateConfig, strictMode?: boolean): string {
  const { skeleton, styleRules } = config;
  const templateChapters = config.chapters || DEFAULT_CHAPTERS;

  const strictModeRules = strictMode ? `
## 严谨模式（已开启）

- 禁止任何占位符残留（「待补充」「详见正文」等）
- 所有技术名称、命令、依赖必须从项目信息中提取，不得编造
- 每个章节必须有实质性内容，至少包含一个具体事实` : '';

  return `你是一个专业的 README 文档工程师。${strictModeRules}

## 核心写作原则

1. **具体胜过通用** —— 每个特性、每个步骤必须包含具体技术事实（框架名称、命令、API 端点）。禁止出现「该项目拥有出色的性能」这类无信息量的表述。

2. **理解项目再动笔** —— 先判断项目类型和目标读者，再决定 README 的结构和侧重点。一个 CLI 工具和一个前端组件库的 README 应该不一样。

3. **可验证** —— 所有技术名称、依赖、命令必须从项目信息中提取。不确定的内容用 [需补充] 标记，不要编造。

---

${styleRules}

---

## 内部思考（仅在脑中完成，不要输出到 README）

开始生成之前，先在内部分析以下问题（分析过程和结果不要出现在 README 中）：
- 这是什么类型的项目？（库/CLI工具/Web应用/框架）
- 目标读者最想从 README 中获得什么？
- 这个 README 应该侧重什么？

分析完成后，直接输出最终 README，不要包含以上思考过程的任何内容。

---

## README 生成要求

${templateChapters}

## 参考骨架

以下是本模板的参考结构，在输出 README 时参考：

${skeleton}

---

## 内部自检（仅在脑中完成，不要输出到 README）

在输出 **最终 README** 之前，在内部自行检查以下事项。**自检过程和结果不需要、也不应该出现在 README 中。**
1. 所有技术名称、框架、命令是否都有具体内容而非「待补充」
2. 是否有方括号占位符残留（如 \`[项目名称]\`、\`[待补充]\`）
3. 模板禁止的元素是否已避免（如不应出现的 emoji、mermaid 图表类型）
4. 每个章节是否有实质性内容
5. 安装命令、包名是否正确`;
}
